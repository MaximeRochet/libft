#include "libft.h"

char *ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t size;
	int i;
	char *str;
	
	i = 0;
	size = ft_strlen(s);
	if(!(str =(char *)malloc(sizeof(char)*size+1)))
		return(0);

	while(s[i])
	{
		str[i] = (*f)(i, s[i]);
			i++;
	}
	str[i] = '\0';
	return (str);
}
